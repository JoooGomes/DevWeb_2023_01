// while
let contador = 1;
while (contador <= 50) {
    console.log(`Valor = ${contador}`);
    contador++;
}
// while com decremento
console.clear();
let cont = 10;
while (cont >= 0) {
    console.log(`Valor = ${cont}`);
    cont--;
}
